// Boleanos
var possuiGraduacao = true;

if (possuiGraduacao) {
  console.log('É verdadeiro!');
} else {
  console.log('É falso!');
}

// Condicionais else if

var possuiGraduacao = true;
var possuiDoutorado = true;

if (possuiGraduacao) {
  console.log('É verdadeiro!');
} else if (possuiDoutorado) {
  console.log('Possui doutorado!');
} else {
  console.log('Não possui nada!');
}